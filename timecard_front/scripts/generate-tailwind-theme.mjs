// generate-tailwind-theme.mjs
import fs from 'fs';
import path from 'path';
import { colors } from '../src/config/colors.js'; // Adjust the import path if needed

let cssContent = `/*\n  THIS FILE IS AUTO-GENERATED.\n  DO NOT EDIT THIS FILE DIRECTLY.\n  EDIT \`src/colors.js\` INSTEAD.\n*/\n\n@theme {\n`;

// Iterate over the color definitions and create CSS custom properties
for (const colorName in colors) {
  for (const shade in colors[colorName]) {
    const value = colors[colorName][shade];
    cssContent += `  --color-${colorName}-${shade}: ${value};\n`;
  }
}

cssContent += '}';

// Write the generated CSS to a file
const outputPath = path.resolve(process.cwd(), './src/tailwind-theme.css'); // Or wherever you want to save it
fs.writeFileSync(outputPath, cssContent);

console.log('âœ… Tailwind theme generated successfully from colors.js');
